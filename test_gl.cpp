void pixel(int cx, int cy) {
	glPointSize(10);
	glColor3f(0, 0, 0);  // Ajusta el color del pixel a negro
	glBegin(GL_POINTS);
	glVertex2f(cx, cy);
	glEnd();
}

void Dps() {
	glClear(GL_COLOR_BUFFER_BIT);

	// Recorre la matriz y dependiendo si encuentra un 1 o un 0 dibuja el pixel.
	for (int i = 0; i < 50; ++i) {
		for (int j = 0; j < 50; ++j) {
			if (matriz[i][j] == 1) {
				// Dibuja un pixel negro en la posici n que le corresponde.
				pixel(i, j);
			}
		}
	}

	glutSwapBuffers();
}

//FUNCION PRINCIPAL
int main(int arg, char* argv[]) {
	//INICIALIZAR LA LIBRERIA OPENGL CON SUS PARAMETROS 
	glutInit(&arg, argv);
	//CONFIGURAR EL MODO DE PANTALLA, ENVIANDO 2 PARAMETROS EL COLORES Y LOS BUFFER
	//A UTILIZAR
	glutInitDisplayMode(GLUT_RGB | GLUT_DOUBLE);
	//CONFIGURAMOS EL ANCHO Y ALTO DE LA VENTANA
	glutInitWindowSize(500, 500);
	//CONFIOGURAMOS LA POSICION DE LA VENTA
	glutInitWindowPosition(100, 100);
	//CREAR VENTANA Y TITULO
	glutCreateWindow("DIBUJA PIXEL");
	//ADECUAR LA VENTANA CREADA PARA DIBUJAR, 4 PARAMETROS
	glClearColor(1, 1, 1, 1);
	gluOrtho2D(0, 50, 50, 0);
	//REDIBUJAR EL CONTENIDO
	glutDisplayFunc(Dps);
	//FUNCION QUE REPETIR EL CICLO
	glutMainLoop();
	return 0;
}